/**
 * @agentx/agent-worker — runs an LLM agent inside a sandboxed,
 * peekable tmux session, with workspace SKILL.md content as the
 * system prompt.
 *
 * Per the agent-worker PRD (.plans/2026-04-30-prd-agent-worker-lib.md):
 *
 *   1. The harness-server spawns this worker for each job phase
 *      (or for the synthetic coordinator/checkout-coordinator
 *      agents when those become real).
 *   2. The worker redirects HOME + TMPDIR to workspace-private dirs
 *      (sandbox isolation; can't stomp host).
 *   3. It opens a tmux session named `agent-<jobId>` so a developer
 *      can `harness attach <jobId>` to watch live.
 *   4. It loads workspace SKILL.md files and concatenates them into
 *      the agent's system prompt.
 *   5. It invokes the agent via @agentx/agent-adapter; tool calls
 *      from the agent (Bash → harness CLI → UDS → triad) flow back
 *      through the SKILL.md-documented surface.
 *   6. SIGTERM shuts down the tmux session; the worker exits.
 *
 * Phase 1 (this commit): scaffold + skill loader + sandbox helpers +
 * tmux session wrapper. The runAgentWorker entry point is wired but
 * the actual LLM invocation is stubbed — it constructs the system
 * prompt and returns a deterministic "no agent runtime wired yet"
 * response. That's enough to validate the SKILL.md → prompt path
 * end-to-end without an API key. Real LLM invocation lands in a
 * follow-up slice when we add a Claude SDK adapter binding.
 */

